package com.biz.log;

public class LoginfoVO {
	private int lseq;
	private String id;
	private String regdate;

	public int getLseq() {
		return lseq;
	}

	public void setLseq(int lseq) {
		this.lseq = lseq;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getRegdate() {
		return regdate;
	}

	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}

}
